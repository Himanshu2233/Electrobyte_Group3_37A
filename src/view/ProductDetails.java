    private void MinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MinActionPerformed
        int qty = Integer.parseInt(Quantity.getText());
        if (qty > 1) {
            qty--;
            Quantity.setText(String.valueOf(qty));
        }
    }//GEN-LAST:event_MinActionPerformed

    private void maxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_maxActionPerformed
        int qty = Integer.parseInt(Quantity.getText());
        qty++;
        Quantity.setText(String.valueOf(qty));
    }//GEN-LAST:event_maxActionPerformed

    private void AddCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddCartActionPerformed
        int userId = model.Session.currentUserId;
        int qty = 1;
        try {
            qty = Integer.parseInt(Quantity.getText());
        } catch (Exception ex) {
            // fallback to 1 if not parsable
        }
        if (product != null) {
            boolean added = model.CartManager.addToCart(userId, product.getId(), qty);
            if (added) {
                JOptionPane.showMessageDialog(this, "Added to cart!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add to cart.");
            }
        }
    }//GEN-LAST:event_AddCartActionPerformed

    private void SendRevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendRevActionPerformed
        int userId = model.Session.currentUserId;
        if (product == null) {
            JOptionPane.showMessageDialog(this, "No product selected.");
            return;
        }
        String reviewText = ReviewBox.getText().trim();
        if (reviewText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a review.");
            return;
        }
        Review review = new Review();
        review.setUserId(userId);
        review.setProductId(product.getId());
        review.setReviewText(reviewText);

        ReviewDao reviewDao = new ReviewDao();
        boolean success = reviewDao.addReview(review);
        if (success) {
            JOptionPane.showMessageDialog(this, "Review submitted!");
            ReviewBox.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to submit review.");
        }
    }//GEN-LAST:event_SendRevActionPerformed

